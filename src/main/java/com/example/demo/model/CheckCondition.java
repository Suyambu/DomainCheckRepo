package com.example.demo.model;

public class CheckCondition {

	int start;
	int end;
	
	
	
	public CheckCondition() {
	
	}
	
	
	public CheckCondition(int start, int end) {
		super();
		this.start = start;
		this.end = end;
	}
	public int getStart() {
		return start;
	}
	public void setStart(int start) {
		this.start = start;
	}
	public int getEnd() {
		return end;
	}
	public void setEnd(int end) {
		this.end = end;
	}
	
	
	
}
